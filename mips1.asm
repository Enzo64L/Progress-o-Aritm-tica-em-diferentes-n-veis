# Este programa em assembly descobre um elemento em uma progressão aritmética
.data	
	r: .asciiz "Informe razão: "
	a1: .asciiz "Informe primeiro elemento: "
	n: .asciiz "Informe elemento desejado: "
	an: .asciiz "O valor desejado é: "
	

.text
	#Imprimir pergunta r
	li $v0,4	#00100100000000100000000000000100
	la $a0,r	#00111100000000010001000000000001
	syscall		#00000000000000000000000000001100
	
	#leitura
	li $v0, 5	#00100100000000100000000000000101
	syscall		#00000000000000000000000000001100
	move $t0 $v0	#00000000000000100100000000100001
	
	#Imprimir pergunta a1
	li $v0,4	#00100100000000100000000000000100
	la $a0,a1	#00111100000000010001000000000001
	syscall		#00000000000000000000000000001100
	
	#leitura
	li $v0, 5	#00100100000000100000000000000101
	syscall		#00000000000000000000000000001100
	move $t1 $v0	#00000000000000100100100000100001
	
	#Imprimir pergunta n
	li $v0,4	#00100100000000100000000000000100
	la $a0,n	#00111100000000010001000000000001
	syscall		#00000000000000000000000000001100
	
	#leitura
	li $v0, 5	#00100100000000100000000000000101
	syscall		#00000000000000000000000000001100
	move $t2 $v0	#00000000000000100101000000100001
	
	li $t3,1	#00100100000010110000000000000001
	
	#Conta
	sub $t2,$t2,$t3		#00000001010010110101000000100010
	mul $t0,$t0,$t2		#01110001000010110100000000000010
	add $t0,$t1,$t0		#00000001001010000100000000100000
	
	#Imprimir resultad0
	li $v0,4	#00100100000000100000000000000100
	la $a0,an	#00111100000000010001000000000001
	syscall		#00000000000000000000000000001100
	
	move $v0 $t0	#00000000000010000001000000100001
	li $v0, 1	#00100100000000100000000000000001
	move $a0,$t0	#00000000000010000010000000100001
	syscall		#00000000000000000000000000001100
	
	li $v0,10	#00100100000000100000000000001010
	syscall		#00000000000000000000000000001100
	